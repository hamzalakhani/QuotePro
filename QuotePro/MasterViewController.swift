        cell.authorCell.text = quoteArray[indexPath.row].quoteAuthor
        cell.cellImage.image = quoteArray[indexPath.row].randomImage



        return cell
    }
    @IBAction func shareButton(_ sender: Any) {
    
        let table = TableViewCell()
